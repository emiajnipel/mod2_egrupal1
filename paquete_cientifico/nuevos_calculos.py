
# área de un rectangulo, dados sus lados

def area_rect(cara_1, cara_2):
    area_rectangular = cara_1*cara_2
    return area_rectangular

# perímetro de un rectangulo, dados sus lados

def perim_rect(cara_1, cara_2):
    perimetro_rectangular = cara_1*2+cara_2*2
    return (perimetro_rectangular)

# distancia recorrida, dado tiempo y velocidad.

def distancia_rec(velocidad, tiempo):
    distancia = velocidad*tiempo
    return distancia
